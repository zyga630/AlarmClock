void isAlive(int speed){
	if(HAL_GetTick()%speed == 0){
		switch (HAL_GPIO_ReadPin(GPIOC, LED)){
			case 0:
				HAL_GPIO_WritePin(GPIOC, LED, GPIO_PIN_SET);
			break;
			case 1:
				HAL_GPIO_WritePin(GPIOC, LED, GPIO_PIN_RESET);
			break;
		}
	}
}


RTC_TimeTypeDef sTime = {0};
RTC_AlarmTypeDef sAlarm = {0};
bool AlarmEvent = false;
void setAlarm(void)
{
	sAlarm.AlarmTime.Hours = 0x00;
	sAlarm.AlarmTime.Minutes = 0x00;
	sAlarm.AlarmTime.Seconds = 0x05;
}

uint8_t freq_table[6] = {120,0,120,0,120,0};
int time_table[6]= {300,300,300,300,300,300};
void playNote(uint8_t freq, int time)
{
	htim1.Instance -> CCR1 = freq;
	HAL_Delay(time);
}
void playAlarm()
{
	while(AlarmEvent == false)
	{
		for(int i=0;i<6;i++)
		{
			playNote(freq_table[i],time_table[i]);
		}
	}
}

void lcd_update(TimeStruct time){
	lcd_clear();
	lcd_return();
	lcd_write(int_to_char(time.hour)+1);
	lcd_write(":");
	lcd_write(int_to_char(time.minute)+1);
	if(state == COUNT) {lcd_write(" DD.MM.YYYY");}
	if(state == SET_TIME){ lcd_write(" AA.MM.YYYY");}
	
	lcd_CursorShift(LEFT, 24);
	lcd_write("23*C");
	lcd_CursorShift(LEFT, 3);
	lcd_write("A!: ");
	lcd_write("HA:MA");
	
}
void CountTime(void){	if(HAL_GetTick()%1000 == 1){ 
		RealTime.second++;		
		if(RealTime.second == 60){
			RealTime.minute++;
			RealTime.second = 0;
			if(RealTime.minute == 60){
				RealTime.hour++;
				RealTime.minute = 0;
				if(RealTime.hour == 24){ 
					RealTime.hour = 0;
				}
			}
		}	
	}	
}

